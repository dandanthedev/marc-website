<h1>
        Vera
        VERA is hèt centrum voor de Popcultuur in Groningen, ook wel omschreven als: Club for the International Pop Underground.
        We waren ooit een keurige studentenvereniging, en daar komt de naam ook vandaan: Veri Et Recti Amici (Ware En Oprechte Vrienden).
        Anno nu is het een volwaardig concertpodium.
        Vele bands, die later soms wereldberoemd werden, hebben hier ooit gestaan, zoals: U2, Joy Division, Simple Minds, Nick Cave,
        Henry Rollins, Sonic Youth, Soundgarden, Slayer, Nirvana, Pearl Jam, Weezer, The White Stripes, At The Drive-in, Mogwai,
        Franz Ferdinand, Editors, Interpol, Elbow, The Hives, Triggerfinger, Nothing But Thieves, Dua Lipa e.v.a.
         <br>
         <br>
         
        Met de neus op de band!
        Vera heeft sinds 2001 een foto- groep. Vrijwilligers fotograferen elk concert. Marc de Krosse is er van het begin af aan bij.
        "Ik sta het liefst met mijn neus op de band. Daarom fotografeer ik graag de kleine concerten in de Kelderbar.
        Ik probeer de energie van zo'n band terug te laten komen in zo'n foto. Dat je de beweging ziet en het zweet van de band
        op de camera voelt. Zoals het optreden van Loud Squirt. Dat was geweldig."
        "Professionele fotografen bij grote concerten moeten vaak na de eerste drie nummers weer weg. Wij blijven een heel optreden,
        dan heb je ook de bezwete hoofden aan het einde. Daar gebeurt veel meer dan aan het begin." We stellen ons altijd van
        tevoren netjes voor aan de band, vragen toestemming om te fotograferen. In die veertien jaar heb ik één keer
        meegemaakt dat een band niet wilde."
    </h1>
